void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    map<int, int> cnt;
    int maxc = 0;
    for (int i = 0; i < n; i++) {
        cnt[a[i]]++;
        maxc = max(maxc, cnt[a[i]]);
    }
    cout << maxc << " " << cnt.size();

} 

